class Demo{
    public static void main(String args[]){
        char color = 'g';
        switch(color)
        {
            case 'r':System.out.println("red");
            break;
            case 'w':System.out.println("white");
            break;
            case 'y':System.out.println("yellow");
            break;
            case 'g':System.out.println("green");
            default :System.out.println("No color");
        }
    }
}